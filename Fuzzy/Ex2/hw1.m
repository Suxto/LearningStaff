clc;clear;close all;
tic;
u1=1:1:6;
u2=[1,6];
A1=-0.14*u1.^2+0.97*u1-0.69;
A2=-0.14*u2.^2+0.97*u2-0.69;
figure;
xmin=min(u1);
xmax=max(u1);
ymax=max(A1);
stem(u1,A1);
axis([xmin-0.2,xmax+0.5,0,1.05*ymax]);
xlabel('x');
ylabel('Membership Function');
box off;
figure;
xmin=min(u2);
xmax=max(u2);
ymax=max(A2);
stem(u2,A2);
axis([xmin-0.2,xmax+0.5,0,1.05*ymax]);
xlabel('x');
ylabel('Membership Function');
box off;
toc;
